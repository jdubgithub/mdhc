<p>Next issue is that Insurance Companies frequently send the&nbsp;Doctors&nbsp;payment for service to the patient’s home address expecting that the patient will give it to their Doctor before the next visit. Patients may cash that Insurance payment for Doctor services. This is a problem because, <span style="text-decoration: underline;">the patient is ultimately responsible for their Co-Pay, and for the Doctors payment.</span> If they don’t turn over the checks to the Doctor their debt will increase, almost unnoticeably. One way to get around this problem is to pay cash for each Doctor’s visit and have us help you to send in your own bill so that you can keep the Doctors payment when the Insurance pays.</p>
