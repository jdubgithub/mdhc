                        </p>
                        <h3>
                          <strong>Summary of The Law:</strong>
                        </h3>
                        <p>
                          <strong>Providers</strong> <strong>are required</strong> to follow the<strong> MTUS </strong>(Medical
                          Treatment Utilization Schedule), which contains a set of <strong>multidisciplinary treatment
                            guidelines</strong> effective for certain injuries, as well as how often the treatment
                          should be given, the extent of the treatment, and for how long, among other things.
                        </p>
                        <p>
                          <strong>Early care</strong> is paramount. It should be approached in an <strong>integrated
                            multidisciplinary </strong>manner that includes treatment modalities such as <strong>medications</strong>,
                          <strong>physical therapy, injections</strong>, and&nbsp;<strong>behavioral modification</strong>
                          with the goal of functional restoration rather than merely the elimination of pain. <a
                            href="http://www.dir.ca.gov/dwc/DWCPropRegs/MTUS_Regulations/MTUS_Regulations.htm"
                            target="_blank">www.dir.ca.gov/dwc/DWCPropRegs/MTUS_Regulations/MTUS_Regulations.htm</a>,
                          Part 1 Introduction, MTUS pg 1
                        </p>
                        <p>
                          <strong>History and Physical: </strong>This should rule out any major “red flags” that would
                          endanger the patient. When red flags are ruled out then Conservative Treatment directed at
                          Myofascial components is indicated.
                        </p>
                        <h3>
                          <strong>Initial Conservative Treatment For Acute Injuries</strong>
                        </h3>
                        <p>
                          <u>Pharmacologic Treatment:</u>
                        </p>
                        <ul>
                          <li>To include analgesics, NSAID’s, muscle relaxants.</li>
                        </ul>
                        <p>
                          <span style="text-decoration: underline;">Behavioral Modification:</span>
                        </p>
                        <ul>
                          <li>Psychotherapy to rule out dual diagnosis, malingering, and prevent chronicity.</li>
                        </ul>
                        <p>
                          <u>Physical Therapy:</u>
                        </p>
                        <ul>
                          <li>3 times per week for 2 weeks to address myofascial components. After this the patients’
                            functionality should have greatly improved unless there is underlying pathology. The intent
                            here is to eliminate or, at least, control any myofascial component to the patients’ pain
                            complaints so that underlying pathology may be identified if present.</li>
                        </ul>
                        <p>
                          <u>Re-Evaluation 3<sup>rd</sup>Week
                          </u>:
                        </p>
                        <ul>
                          <li><strong>Responding:</strong>&nbsp;If the patients’ myofascial component is responding as
                            expected there is probably no other underlying pathology so continue the physical therapy
                            for 2 weeks and re-evaluate for rehabilitation or discharge.</li>
                        </ul>
                        <p>Total physical therapy =&nbsp; 3 per week for 4 weeks = 12 therapies</p>
                        <ul>
                          <li><strong>Not Responding:</strong>&nbsp;If the patient continues to have pain that persists
                            beyond the anticipated time of healing the chronic pain medical treatment guidelines apply.</li>
                        </ul>
                        <p>
                          <span style="text-decoration: underline;">If self sustaining Trigger Points are found they
                            should be injected while continuing physical therapy and re-evaluate after 1 week.</span>
                        </p>
                        <ul>
                          <li><strong>Responding:&nbsp;</strong>If the patients’ myofascial component is responding as
                            expected no other underlying pathology is anticipated so continue the physical therapy for 2
                            more weeks and re-evaluate for rehabilitation or discharge.</li>
                        </ul>
                        <p>Total physical therapy =&nbsp; 3 per week for 6 weeks = 18 therapie</p>
                        <ul>
                          <li><strong>Not Responding:</strong> If the patients’ myofascial component is still not
                            responding as expected search for underlying pathology through imaging, EMG/NCV,
                            psychological evaluation, consultations, and labs followed by treatment modalities such as
                            injections, surgery, etc.&nbsp;While this workup is being done it is important to continue
                            Physical Therapy 2 times per week to not lose the gains that have already been made.</li>
                        </ul>
                        <p>Total physical therapy = 3 per week for 4 weeks plus 2 per week for 6 weeks = 24 therapies.</p>
